#include <stdio.h>

int main() {
    int num1, num2, result;

    // Input two integers
    printf("%d",num1);
    scanf("%d", &num1);

    printf("%d",num2);
    scanf("%d", &num2);

    // Perform bitwise OR operation
    result = num1 | num2;

    // Print the result
    printf("%d",result);

    return 0;
}
